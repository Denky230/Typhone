package com.stucom.grupo4.typhone.model;

public class Letter {

    private char character;
    private float x;

    Letter(char character) {
        this.character = character;
    }

    public char getCharacter() {
        return this.character;
    }

    public void setX(float x) {
        this.x = x;
    }
    public float getX() {
        return this.x;
    }
}
